package machine;

public class Coffee {

    public int water;
    public int milk;
    public int coffeeBeans;
    public int disposableCups;
    public int money;
    public Coffee(int water, int milk, int coffeeBeans, int disposableCups, int money) {
        this.water = water;
        this.milk = milk;
        this.coffeeBeans = coffeeBeans;
        this.disposableCups = disposableCups;
        this.money = money;
    }


}
